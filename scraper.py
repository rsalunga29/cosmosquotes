if __name__ == "__main__":
    ap = argparse.ArgumentParser()
    ap.add_argument('-p', '--pages', default=1,
        type=int, help='Number of Goodreads Cosmos Quotes pages to scrape')
    args = ap.parse_args()

    data = []
    filename = 'static/quotes.json'

    if not os.path.exists(filename):
        open(filename, 'w')

    for page in range(0, args.pages):
        url = f'https://www.goodreads.com/work/quotes/3237312-cosmos?page={page+1}'

        headers = {'User-Agent': randomize_user_agent()}
        response = requests.get(url=url, headers=headers)

        bs = BeautifulSoup(response.text, 'lxml')
        quotes = bs.findAll('div', class_='quoteText')

        for q in quotes:
            quote = q.contents[0].encode('ascii', 'ignore').decode('ascii', 'ignore').strip()

            # Pick only quotes with less than 500 characters
            if len(quote) <= 500:
                data.append({
                    'text': quote,
                })

    print(f'Dumping {len(data)} quotes...')

    with open(filename, 'w') as f:
        json.dump(data, f, indent=4)
